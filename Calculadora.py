class Calculadora(object):

    def __init__(self, a, b):
      self.a = a
      self.b = b

    def divisao(self):
      return self.a / self.b

    def multiplicacao(self):
        return (self.a * self.b)

    def soma(self):
        return self.a + self.b    

    def divisao(self):
      return self.a / self.b

